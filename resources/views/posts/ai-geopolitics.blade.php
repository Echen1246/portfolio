        <article class="post-content">
            <h2>The Geopolitics of AI</h2>
            <p>
                There appears to be a false consensus in Washington DC on how we should proceed with GPU exports and technology policy. I'm writing this to challenge the prevailing assumptions about our current approach and its likely outcomes.
            </p>
            
            <p>
                There is simply too much overconfidence in the idea that Nvidia will remain an absolute necessity to China. Howard Lutnick, USG officials, and many political and business leaders have voiced the strategic need for Nvidia dominance in the East. However, this assumption is already being challenged by reality.
            </p>
            
            <p>
                Huawei's Ascend AI stack has become roughly competitive with the H100, which remains the current standard for American AI data centers. While Eastern chips are still behind our flagship technologies like Blackwell by approximately two years, the progress being made suggests this gap may soon narrow significantly. China's technological development follows a vertical trajectory of rapid improvement.
            </p>
            
            <p>
                Moreover, we must consider the practical constraints on American advancement. Blackwell, while an incredible chip, faces real-world deployment limitations. US data centers cannot instantly replace their existing infrastructure - this represents an incredibly expensive undertaking. Additionally, Nvidia faces its own production capacity constraints.
            </p>

            <h2>Miscalculation</h2>
            <p>
                It has become apparent to the Trump administration that, with the release of the Ascend stack, China's ability to function without Nvidia is increasingly clear. Consequently, their attention has shifted from preventing any advanced chips from reaching China to creating a 'dam' to prevent Chinese chips from flooding foreign markets - which will likely happen regardless.
            </p>
            
            <p>
                This shift reveals a fundamental strategic miscalculation. American R&D is funded by corporations, and Nvidia needs massive sales volumes to continue its investment in innovation. If overseas markets are captured by Eastern competitors, the consequences for American technological leadership become predictable.
            </p>
            
            <p>
                China represents a massive market. For the US to surrender this market is tantamount to surrendering market share everywhere else. More critically, China has evolved from the outdated "cheap Made in China" stereotype to become a global innovation leader in the most important technological fields. The investment scale and talent pool are enormous.
            </p>

            <h2>Self-Sufficiency</h2>
            <p>
                If pressed, China does not require American infrastructure at all. This outcome was predictable: starving their chip ecosystem would only backfire on America. In a country with over 1 billion people, where a significant portion of global AI talent is Chinese, coupled with an already-massive manufacturing foundation, self-sufficiency in chip technology was always inevitable.
            </p>
            
            <p>
                A cooperative approach, or at least a "leash" strategy where companies relying on Nvidia chips would find switching away difficult, would have been far more effective. Unfortunately, that opportunity has passed. With growing Eastern self-sufficiency, our only remaining option is attempting to widen the technological gap between us and our new competitor.
            </p>
            
            <p>
                The argument that "we need to prevent Nvidia chips from being used in their military" reflects a fundamental misunderstanding. Consider this: would the US military ever use a foreign country's chips in our missiles? This would never happen. China, like any major power, prioritizes domestic sourcing for critical military applications. Everything China does centers on domestic technological independence.
            </p>

            <h2>Infrastructure</h2>
            <p>
                Recent developments underscore the urgency of this competition. Over the past week, two new open-source models released in China have rivaled American open and closed-source models. They have published breakthrough indium selenide chip fabrication technology that could obsolete traditional silicon transistor design, and begun construction on enormous hydropower projects to upgrade their power grid.
            </p>
            
            <p>
                The AI competition will ultimately be won on electricity generation and grid capacity. Beyond the Trump administration's focus on fossil fuels and chip export restrictions, what concrete progress has been made on the infrastructure front?
            </p>
            
            <p>
                How do our leaders propose to power current and next-generation AI technology? Our electrical grids are antiquated and barely support existing data centers. These grids will not grow adequately, thanks to fossil fuel prioritization and the sudden halt in renewable energy investment.
            </p>

            <h2>Education</h2>
            <p>
                How can we innovate while cutting educational programs, especially in science and technology? How can we attract students pursuing advanced degrees when we make our educational and research environment hostile to foreign students?
            </p>
            
            <p>
                Simply put, we cannot. While our technology leaders are among the best in the world and are doing their utmost, they can only accomplish so much independently. AI represents the most important innovation of the century, yet our educational institutions do not reflect this priority until early-to-middle university levels. Meanwhile, East Asian and Indian students learn algorithms from elementary school.
            </p>
            
            <p>
                With our current leadership approach, and with our own open-source models struggling to match Eastern competition, the fundamental building blocks of future innovation are being undermined.
            </p>

            <h2>Open Source</h2>
            <p>
                One critical difference requires emphasis: the business ecosystem in China operates differently from America's. We have industry leaders like OpenAI and Anthropic, but these are not open-source platforms. Meanwhile, the East has adopted a comprehensive open-source approach.
            </p>
            
            <p>
                Starting with DeepSeek, which rivaled American models during its release months ago, new open-source models like Kimi and Qwen are rivaling or surpassing their American counterparts while leveraging all developers who use their systems for improvements. This represents a critical advantage: companies like OpenAI employ several dozen brilliant researchers working continuously. Yet open-source models can tap into millions of users for feedback, suggested improvements, and collaborative development.
            </p>

            <h2>Conclusion</h2>
            <p>
                We are not "all in" on this competition - not even close. Our priorities remain scattered, our administration distracted. Trump no longer holds the "keys to give away," and our own leaders are undermining the technological exceptionalism that has powered American leadership for decades.
            </p>
            
            <p>
                As the saying goes: "A society grows great when old men plant trees in whose shade they will never sit." Our current approach suggests we are doing the opposite - cutting down the trees that future generations will need for shade. Our leaders need to wake up to this reality before it becomes irreversible.
            </p>
